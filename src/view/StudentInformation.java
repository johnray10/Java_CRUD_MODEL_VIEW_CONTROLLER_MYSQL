    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed

        Boolean id = !txt_id.getText().isEmpty();

        if (id) {
            model.Students users = new Students();
            users.setId(Integer.parseInt(txt_id.getText()));

            //CONTROLLER DELETE
            StudentController studentController = new StudentController();
            studentController.deleteInformation(users);

            //REFRESHDATA
            showTable();
            JOptionPane.showMessageDialog(rootPane, "Has been Deleted!");

            //CLEARTEXT
            showClear();
        } else {

            JOptionPane.showMessageDialog(rootPane, "Please Select a index row and delete properly");
        }

    }//GEN-LAST:event_btn_deleteActionPerformed

    public void getInformation(int index) {
        StudentController studentController = new StudentController();
        txt_id.setText(Integer.toString(studentController.displayStudents().get(index).getId()));
        txt_name.setText(studentController.displayStudents().get(index).getName());
        txt_email.setText(studentController.displayStudents().get(index).getEmail());
        txt_pass.setText(studentController.displayStudents().get(index).getPassword());

        String gen = studentController.displayStudents().get(index).getGender();
        if ("Male".equals(gen)) {
            radio_male.setSelected(true);

        } else if ("Female".equals(gen)) {
            radio_female.setSelected(true);
        }
        txt_address.setText(studentController.displayStudents().get(index).getAddress());
    }

