    private void insertStudent() {
        String name = txt_name.getText().toUpperCase();
        String email = txt_email.getText();
        String pass = txt_pass.getText();
        radio_male.setActionCommand("Male");
        radio_female.setActionCommand("Female");
        String gen = buttonGroup1.getSelection().getActionCommand();
        String address = txt_address.getText();

        model.Students users = new Students();
        users.setName(name);
        users.setEmail(email);
        users.setPassword(pass);
        users.setGender(gen);
        users.setPassword(pass);
        users.setAddress(address);

        //CONTROLLER SAVE
        StudentController studentController = new StudentController();
        studentController.saveInformation(users);

        //REFRESHDATA
        showTable();
        JOptionPane.showMessageDialog(rootPane, "Has been Created");

        //CLEARTEXT
        showClear();
    }

    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed

        if (txt_name.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Name cannot be empty", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            getToolkit().beep();
            txt_name.requestFocus();

        } else if (!Pattern.matches("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$", txt_email.getText())) {
            JOptionPane.showMessageDialog(null, "Please enter a valid email", "Error", JOptionPane.ERROR_MESSAGE);
            getToolkit().beep();
            txt_email.requestFocus();

        } else if (txt_pass.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Password cannot be empty", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            getToolkit().beep();
            txt_pass.requestFocus();

        } else if (txt_address.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Address cannot be empty", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            getToolkit().beep();
            txt_address.requestFocus();

        } else if (radio_male.isSelected() == false && radio_female.isSelected() == false) {
            JOptionPane.showMessageDialog(rootPane, "Gender cannot be empty", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            getToolkit().beep();

        } else {
            insertStudent();
        }

    }//GEN-LAST:event_btn_insertActionPerformed

