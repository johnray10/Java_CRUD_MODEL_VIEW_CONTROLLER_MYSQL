    public void showTable() {
        StudentController studentController = new StudentController();
        ArrayList<Students> list = studentController.displayStudents();

        DefaultTableModel model = (DefaultTableModel) tbl_studentsList.getModel();
        model.setRowCount(0);
        Object[] row = new Object[6];

        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getName();
            row[2] = list.get(i).getEmail();
            row[3] = list.get(i).getPassword();
            row[4] = list.get(i).getGender();
            row[5] = list.get(i).getAddress();
            model.addRow(row);
            totalrecords.setText("Total Records # : " + tbl_studentsList.getRowCount());

        }

    }

